print("=========================KERUCUT==========================")


pi = 3.14
radius = 2
s = 4
t = 5

luas_selimut = pi * radius * s
luas_permukaan = pi * radius * s + pi * radius**2

volume = (1/3) * pi * radius**2 * t

print("Luas selimut kerucut : ", luas_selimut)
print("Luas permukaan kerucut : ", luas_permukaan)

print("Volume kerucut : ", volume)